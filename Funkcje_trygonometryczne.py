import math

class Kalkulator:

    def sinus(x):
        return math.sin(x)
    

    def cosinus(x):
        return math.cos(x)
    

    def tangens(x):
        return math.tan(x)
    

    def cotangens(x):
        return 1 / math.tan(x)
    

    def arcsinus(x):
        return math.asin(x)
    

    def arccosinus(x):
        return math.acos(x)
    

    def arctangens(x):
        return math.atan(x)
    

    def arccotangens(x):
        return math.atan(1 / x)

    def sinh(x):
        return math.sinh(x)


    def cosh(x):
        return math.cosh(x)

    def tanh(x):
        return math.tanh(x)

    def asinh(x):
        return math.asinh(x)


    def acosh(x):
        return math.acosh(x)


    def atanh(x):
        return math.atanh(x)